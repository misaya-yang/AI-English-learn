export const getRandomWords = (count: number): WordData[] => {
  const shuffled = [...wordsDatabase].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

export const getWordOfTheDay = (): WordData => {
  // 根据日期选择单词，确保每天不同
  const today = new Date();
  const dayOfYear = Math.floor((today.getTime() - new Date(today.getFullYear(), 0, 0).getTime()) / (1000 * 60 * 60 * 24));
  const index = dayOfYear % wordsDatabase.length;
  return wordsDatabase[index];
};

export const getPreviousWords = (count: number = 7): { date: string; word: WordData }[] => {
  const result: { date: string; word: WordData }[] = [];
  const today = new Date();
  
  for (let i = 1; i <= count; i++) {
    const date = new Date(today);
    date.setDate(date.getDate() - i);
    const dayOfYear = Math.floor((date.getTime() - new Date(date.getFullYear(), 0, 0).getTime()) / (1000 * 60 * 60 * 24));
    const index = dayOfYear % wordsDatabase.length;
    result.push({
      date: date.toISOString().split('T')[0],
      word: wordsDatabase[index]
    });
  }
  
  return result;
};

